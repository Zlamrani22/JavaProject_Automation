package session_2ndOctober2022;

public class TryingToPrint {
    public static void main(String[] args) {

        System.out.println(1); //Prints 1 and moves the cursor to the next line
        System.out.println(2); //prints 2 and moves to a new line
        System.out.print(3); //print 3 but cursor will not move to a new line, it stays there
        System.out.println(4);//prints 4 and moves to a new line
        System.out.print(5);//prints 5 but cursor did not go to a new line
        System.out.println(6);//will print 6 and cursor moves to the next line
        System.out.println(7);

    }

}
